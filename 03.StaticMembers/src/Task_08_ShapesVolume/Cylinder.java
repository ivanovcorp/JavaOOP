public class Cylinder {
    private Double radius;
    private Double height;

    public Cylinder(Double radius, Double height) {
        this.radius = radius;
        this.height = height;
    }

    public Double getRadius() {
        return this.radius;
    }

    public Double getHeight() {
        return this.height;
    }
}
